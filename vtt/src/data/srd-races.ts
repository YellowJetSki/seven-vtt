export function getRaceById(id: string): RaceDefinition | undefined {
  return SRD_RACES.find(r => r.id === id);
}

export function getRaceByName(name: string): RaceDefinition | undefined {
  return SRD_RACES.find(r => r.name.toLowerCase() === name.toLowerCase());
}

export function getRaceNames(): string[] {
  return SRD_RACES.map(r => r.name);
}
